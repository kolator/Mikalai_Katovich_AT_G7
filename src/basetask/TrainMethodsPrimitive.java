public class TrainMethodsPrimitive {
    public static void main(String[] args) {

    }

    public void printInt(int prl) {
        System.out.println("я получил на вход число" + prl);
    }

    public void printLong(long prl) {
        System.out.println("я получил на вход длинное число" + prl);
    }

    public void printChar(char prl) {
        System.out.println("я получил на вход символ" + prl);
    }

    public void printFloat(float prl) {
        System.out.println("я получил на вход дробное число" + prl);
    }

    public void printDouble(double prl) {
        System.out.println("я получил на вход длинное дробное число" + prl);
    }

    public void printShort(short prl) {
        System.out.println("я получил на вход короткое число" + prl);
    }

    public void printByte(byte prl) {
        System.out.println("я получил на вход очень короткое число" + prl);
    }

    public void printBoolean(boolean prl) {
        System.out.println("я получил на вход булево" + prl);
    }
}
